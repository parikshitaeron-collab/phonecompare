/** Case-insensitive partial match on a target string */
export function fuzzyMatch(target = "", query = "") {
  return target.toLowerCase().includes(query.toLowerCase());
}

/** Find a single phone by ID (exact) or name (fuzzy) */
export function findPhone(identifier) {
  const id = (identifier || "").toLowerCase().trim();
  return (
    PHONES.find((p) => p.id === id) ||
    PHONES.find((p) => p.name.toLowerCase() === id) ||
    PHONES.find((p) => fuzzyMatch(p.name, identifier))
  );
}

/** Apply brand / tier / price filters + sorting to a phone list */
export function filterAndSort(phones, { brand, tier, minPrice, maxPrice, sort } = {}) {
  let result = [...phones];

  if (brand)    result = result.filter((p) => p.brand.toLowerCase() === brand.toLowerCase());
  if (tier)     result = result.filter((p) => p.tier  === tier.toLowerCase());
  if (minPrice) result = result.filter((p) => p.price >= Number(minPrice));
  if (maxPrice) result = result.filter((p) => p.price <= Number(maxPrice));

  switch (sort) {
    case "price_asc":  result.sort((a, b) => a.price - b.price); break;
    case "price_desc": result.sort((a, b) => b.price - a.price); break;
    case "newest":     result.sort((a, b) => b.year  - a.year);  break;
    default:           result.sort((a, b) => b.scores.overall - a.scores.overall);
  }

  return result;
}

/** Score a category result into human-readable detail text */
export function scoreDetail(phone, category) {
  const score = phone.scores[category];
  const tier =
    score >= 9.5 ? "best-in-class"
    : score >= 9.0 ? "flagship-tier"
    : score >= 8.0 ? "excellent"
    : score >= 7.0 ? "solid"
    : "average";
  return `${phone.name} scores ${score}/10 on ${category} — ${tier} ${category} performance.`;
}
