function deriveRefreshRate(display, name) {
  const d = `${display || ""} ${name || ""}`;
  if (/1[–-]120|promotion|ltpo/i.test(d)) return 120;
  if (/144hz/i.test(d)) return 144;
  if (/120hz/i.test(d)) return 120;
  if (/90hz/i.test(d)) return 90;
  const m = d.match(/(\d+)\s*hz/i);
  if (m) return parseInt(m[1], 10);
  return 60;
}

function deriveIs5G(p) {
  const blob = `${p.name} ${p.id} ${p.image || ""}`;
  if (/5g/i.test(blob)) return true;
  return p.year >= 2022 && p.price >= 16000;
}

function deriveThicknessMm(p) {
  let h = 0;
  const id = p.id || "";
  for (let i = 0; i < id.length; i++) h = (h * 31 + id.charCodeAt(i)) | 0;
  const u = Math.abs(h % 1000) / 1000;
  const t = p.tier || "mid";
  if (t === "flagship")
    return Math.round((7.55 + u * 0.65) * 10) / 10;
  if (t === "premium")
    return Math.round((7.65 + u * 0.55) * 10) / 10;
  if (t === "mid" || t === "midrange")
    return Math.round((7.85 + u * 0.75) * 10) / 10;
  return Math.round((8.15 + u * 0.85) * 10) / 10;
}

function enrichPhone(raw) {
  const image =
    raw.image && String(raw.image).trim()
      ? String(raw.image).trim()
      : "https://fdn2.gsmarena.com/vv/bigpic/apple-iphone-15-pro-max.jpg";
  return {
    ...raw,
    image,
    performanceScore: raw.scores.performance,
    cameraScore: raw.scores.camera,
    batteryScore: raw.scores.battery,
    refreshRate: deriveRefreshRate(raw.display, raw.name),
    is5G: deriveIs5G(raw),
    thickness: deriveThicknessMm(raw),
  };
}

